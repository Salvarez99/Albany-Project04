package project04;

public interface ClassAttributesInterface {

	/*
	 * gets class number
	 * @return int classNumber
	 */
	public int getClassNumber();
	
	/*
	 * sets classNumber
	 * @param int classNumber
	 */
	public void setClassNumber(int classNumber);
	
	/*
	 * gets CourseSubject
	 * @return String CourseSubject
	 */
	public String getCourseSubject();
	
	/*
	 * sets CourseSubject
	 * @param String CourseSubject
	 */
	public void setCourseSubject(String courseSubject);
	
	/*
	 * gets CatalogNumber
	 * @return int CatalogNumber
	 */
	public int getCatalogNumber();
	
	/*
	 * sets CatalogNumber
	 * @param int CatalogNumber
	 */
	public void setCatalogNumber(int catalogNumber);
	
	/*
	 * gets ClassTitle
	 * @return String ClassTitle
	 */
	public String getClassTitle();
	
	/*
	 * sets ClassTitle
	 * @param String ClassTitle
	 */
	public void setClassTitle(String classTitle);
	
	/*
	 * gets Level
	 * @return String Level
	 */
	public String getLevel();
	
	/*
	 * sets Level
	 * @param String Level
	 */
	public void setLevel(String level);
}
